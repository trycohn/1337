                isWinner: Boolean(isWinner),
                status: status || 'NO_SHOW',
                name: participantInfo?.name || 'TBD',
                score: resultText,
                avatarUrl: participantInfo?.avatar_url ? ensureHttps(participantInfo.avatar_url) : null
